import React, { createContext, useContext, useState, useEffect, ReactNode } from 'react';
import { CartItem } from '../types/cart';

// Interface pour les dÃ©limitations
interface DelimitationData {
  x: number;
  y: number;
  width: number;
  height: number;
  coordinateType: 'PERCENTAGE' | 'PIXEL';
}

interface CartContextType {
  items: CartItem[];
  itemCount: number;
  isOpen: boolean;
  addToCart: (product: {
    id: number;
    name: string;
    price: number;
    color: string;
    colorCode: string;
    size: string;
    imageUrl: string;
    designUrl?: string;
    vendorName?: string;
    // PropriÃ©tÃ©s pour afficher le design
    designId?: number;
    adminProductId?: number;
    designScale?: number;
    delimitations?: DelimitationData[];
    // PropriÃ©tÃ©s pour les vraies tailles de la base de donnÃ©es
    selectedSize?: {
      id: number;
      name: string;
    };
    sizeId?: number;
    sizeName?: string;
  }) => void;
  removeFromCart: (id: string) => void;
  updateQuantity: (id: string, quantity: number) => void;
  openCart: () => void;
  closeCart: () => void;
  clearCart: () => void;
}

const CartContext = createContext<CartContextType | undefined>(undefined);

interface CartProviderProps {
  children: ReactNode;
}

export const CartProvider: React.FC<CartProviderProps> = ({ children }) => {
  const [items, setItems] = useState<CartItem[]>([]);
  const [isOpen, setIsOpen] = useState(false);

  // Charger le panier depuis le localStorage au dÃ©marrage
  useEffect(() => {
    const savedCart = localStorage.getItem('cart');
    if (savedCart) {
      try {
        setItems(JSON.parse(savedCart));
      } catch (error) {
        console.error('Erreur lors du chargement du panier:', error);
        localStorage.removeItem('cart');
      }
    }
  }, []);

  // Sauvegarder le panier dans le localStorage Ã  chaque modification
  useEffect(() => {
    if (items.length > 0) {
      localStorage.setItem('cart', JSON.stringify(items));
    } else {
      localStorage.removeItem('cart');
    }
  }, [items]);

  const itemCount = items.reduce((sum, item) => sum + item.quantity, 0);

  const addToCart = (product: {
    id: number;
    name: string;
    price: number;
    color: string;
    colorCode: string;
    size: string;
    imageUrl: string;
    designUrl?: string;
    vendorName?: string;
    designId?: number;
    adminProductId?: number;
    designScale?: number;
    delimitations?: DelimitationData[];
    selectedSize?: {
      id: number;
      name: string;
    };
    sizeId?: number;
    sizeName?: string;
  }) => {
    console.log('ðŸ›’ [CartContext] Ajout au panier:', product);
    // Utiliser la vraie taille si disponible, sinon la taille de base
    const sizeValue = product.selectedSize?.name || product.sizeName || product.size;
    const cartItemId = `${product.id}-${product.color}-${sizeValue}`;
    console.log('ðŸ›’ [CartContext] CartItem ID:', cartItemId);
    console.log('ðŸ›’ [CartContext] Taille utilisÃ©e:', sizeValue);

    setItems(prevItems => {
      console.log('ðŸ›’ [CartContext] Articles prÃ©cÃ©dents:', prevItems.length);
      const existingItem = prevItems.find(item => item.id === cartItemId);

      if (existingItem) {
        // Si le produit existe dÃ©jÃ , augmenter la quantitÃ©
        console.log('ðŸ›’ [CartContext] Produit existant, incrÃ©mentation quantitÃ©');
        return prevItems.map(item =>
          item.id === cartItemId
            ? { ...item, quantity: item.quantity + 1 }
            : item
        );
      } else {
        // Sinon, ajouter le nouveau produit
        console.log('ðŸ›’ [CartContext] Nouveau produit, ajout au panier');
        const newItem: CartItem = {
          id: cartItemId,
          productId: product.id,
          name: product.name,
          price: product.price,
          color: product.color,
          colorCode: product.colorCode,
          size: product.size,
          imageUrl: product.imageUrl,
          designUrl: product.designUrl,
          vendorName: product.vendorName,
          quantity: 1,
          // Nouvelles propriÃ©tÃ©s pour afficher le design
          designId: product.designId,
          adminProductId: product.adminProductId,
          designScale: product.designScale,
          delimitations: product.delimitations,
          // PropriÃ©tÃ©s pour les vraies tailles de la base de donnÃ©es
          selectedSize: product.selectedSize,
          sizeId: product.sizeId,
          sizeName: product.sizeName
        };
        return [...prevItems, newItem];
      }
    });

    // Ouvrir le panier aprÃ¨s ajout
    console.log('ðŸ›’ [CartContext] Ouverture du panier');
    setIsOpen(true);
  };

  const removeFromCart = (id: string) => {
    setItems(prevItems => prevItems.filter(item => item.id !== id));
  };

  const updateQuantity = (id: string, quantity: number) => {
    if (quantity <= 0) {
      removeFromCart(id);
      return;
    }

    setItems(prevItems =>
      prevItems.map(item =>
        item.id === id ? { ...item, quantity } : item
      )
    );
  };

  const openCart = () => setIsOpen(true);
  const closeCart = () => setIsOpen(false);
  const clearCart = () => setItems([]);

  const value: CartContextType = {
    items,
    itemCount,
    isOpen,
    addToCart,
    removeFromCart,
    updateQuantity,
    openCart,
    closeCart,
    clearCart
  };

  return (
    <CartContext.Provider value={value}>
      {children}
    </CartContext.Provider>
  );
};

export const useCart = () => {
  const context = useContext(CartContext);
  if (context === undefined) {
    throw new Error('useCart must be used within a CartProvider');
  }
  return context;
};

export default CartProvider;